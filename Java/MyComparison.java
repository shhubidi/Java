public class MyComparison {
    public static void main(String args[]){
        int x =60;
        int y = 13;
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x&y);
        System.out.println(x|y);
        System.out.println(~x);
        System.out.println(x<<2);
        System.out.println(x>>2);
        System.out.println(x>>>2);
    }
}
